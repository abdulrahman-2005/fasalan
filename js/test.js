function DDD111(rencedCommand) {
	let data = "";
	for (character in rencedCommand) {
		i = rencedCommand[character];
		if (i === "*") {
			data += "0";
		} else if (i === "-") {
			data += "1";
		} else if (i !== "*" && i !== "-" && i !== "=") {
			data += i;
		} else {
			data += " ";
		}
	}
	return data;
}

function decryptitinsomereallybadway(command) {
	command = DDD111(command);
	let data = "";
	for (character in command.split(" ")) {
		let i = command.split(" ")[character];
		try {
			data += binary[i];
		} catch (error) {
			console.log("error");
		}
	}
	return data;
}

